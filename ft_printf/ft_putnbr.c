#include "printf.h"

int	ft_putnbr(int n)
{
	long	no;

	no = n;
	if (no < 0)
	{
		no = no * -1;
		ft_putchar('-');
	}
	if (no >= 10)
	{
		ft_putnbr(no / 10);
	}
	ft_putchar(no % 10 + '0');
	return (0);
}
